static uint32_t _mod_hash(size_t hsize, size_t dsize, void *data) {
   size_t i, k; 
   int m = primes[PRIMES_SIZE - 1];
   uint8_t *dcpy = NULL;

   /* 
      The prime numbers table is not large enough. Instead of fail, 
      we'll warn the user and use the largest prime in the table.
    */
   if (hsize > m) {
      DSC_ERROR(
         "WARNING: Hashtable size is larger than primes table. "
         "Proceeding with largest prime number available..."
      );
   }

   /* It is best to use prime numbers which are close to powers of 2 */
   for (i = PRIMES_SIZE - 2; m > hsize; --i) 
      m = primes[i];

   /* Make a deep copy of data */
   dcpy = alloca(dsize);
   /* TODO error handling */
   memmove(dcpy, data, dsize);
   for (i = 0; i < dsize; ++i) {
      k += dcpy[i]; 
   }

   return k % m;
}

static uint32_t _knuth_mul_hash(size_t hsize, size_t dsize, void *data) {
   size_t i, k;
   long a = 2654435769L;
   uint8_t *dcpy = NULL;

   /* Make a deep copy of data */
   dcpy = alloca(dsize);
   memmove(dcpy, data, dsize);
   for (i = 0; i < dsize; ++i) {
      k += dcpy[i];
   }

   /* 
      NOTE: I don't care about shifting to get the higher p bits because 
      we're returning a uint32, in which case 32 - p = 0.
    */
   return k * a;
}

